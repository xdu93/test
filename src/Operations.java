public class Operations {
    //Операции
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int minus(int a, int b) {
        return a - b;
    }

    public static int multi(int a, int b) {
        return a * b;
    }

    public static int del(int a, int b) {
        return (a / b);
    }
}
